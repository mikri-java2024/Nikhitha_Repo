package mytask;
public class Pattern 
{

	static int i = 6;
	static int s = 6;
	static int r = 6;
	
	public static void main(String[] args) 
	{
		
	
		Pattern.pat();
		
	}
	
	
static void pat()
	{



	for (i=0; i<r; i++)

		{

		for (s=0; s<=i; s++)

			{

				System.out.print("* ");

			}

		System.out.println();


		}

}
	
	
}